/* ══ Engine (ລາວ only) ════════════════════════════════════ */

const LAO_FONT_URL = 'https://fonts.googleapis.com/css2?family=Noto+Sans+Lao:wght@400;500;600;700;800&display=swap';

function t(key) {
  return LAO[key] ?? key;
}

function _loadLaoFont() {
  if (document.querySelector(`link[href*="Noto+Sans+Lao"]`)) return;
  const link = document.createElement('link');
  link.rel  = 'stylesheet';
  link.href = LAO_FONT_URL;
  document.head.appendChild(link);
}

function _applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key  = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    const val  = t(key);
    if (attr) el.setAttribute(attr, val);
    else      el.textContent = val;
  });
  document.documentElement.lang = 'lo';
}

document.addEventListener('DOMContentLoaded', () => {
  _loadLaoFont();
  _applyTranslations();
});

window.RubyI18n = { t };
